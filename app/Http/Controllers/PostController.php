     * @return \Illuminate\Http\Response
     */
    public function index()
    {

        return Post::select([
            '*',
            'username' => User::selectRaw('username')->whereColumn('id', 'user_id'),
            'avatar' => User::selectRaw('avatar')->whereColumn('id', 'user_id'),
        ])->withCount(['comments'])->get();
